#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	char	*str;
	size_t	start;
	size_t	end;
	size_t	str_len;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	str_len = end - start;
	str = (char *)ft_calloc(str_len + 1, sizeof(char));
	if (!str)
		return (NULL);
	ft_memcpy(str, s1 + start, str_len);
	return (str);
}

